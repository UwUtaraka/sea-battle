void Game::autoPlacement() {
    int presets[10][7][4] = {
        {{0,0,4,0}, {0,9,3,1}, {9,0,2,0}, {9,4,2,0}, {5,5,1,0}, {7,7,1,0}, {2,2,1,0}},
        {{0,0,4,1}, {0,2,3,1}, {0,4,2,1}, {0,6,2,1}, {0,8,1,0}, {2,8,1,0}, {4,8,1,0}},
        {{4,3,4,0}, {2,1,3,0}, {6,1,3,1}, {8,5,2,0}, {1,7,1,0}, {4,8,1,0}, {7,8,1,0}},
        {{0,0,4,0}, {2,2,3,0}, {5,5,2,0}, {8,8,2,0}, {0,9,1,0}, {9,0,1,0}, {5,0,1,0}},
        {{0,0,4,0}, {0,5,3,0}, {3,0,2,1}, {3,9,2,1}, {9,0,1,0}, {9,5,1,0}, {9,9,1,0}},
        {{0,1,4,0}, {2,1,3,0}, {4,1,2,0}, {4,4,2,0}, {6,1,1,0}, {6,3,1,0}, {6,5,1,0}},
        {{1,1,4,1}, {1,3,3,1}, {1,5,2,1}, {1,7,2,1}, {8,1,1,0}, {8,3,1,0}, {8,5,1,0}},
        {{9,0,4,0}, {7,0,3,0}, {5,0,2,0}, {5,4,2,0}, {3,0,1,0}, {3,2,1,0}, {3,4,1,0}},
        {{0,0,4,0}, {2,7,3,0}, {4,0,2,0}, {6,7,2,0}, {8,0,1,0}, {0,9,1,0}, {9,9,1,0}},
        {{1,2,4,0}, {3,5,3,1}, {8,2,2,0}, {6,0,2,1}, {0,0,1,0}, {9,9,1,0}, {5,2,1,0}}
    };

    int choice = rand() % 10;

    for (int i = 0; i < 7; i++) {
        int x = presets[choice][i][0];
        int y = presets[choice][i][1];
        int len = presets[choice][i][2];
        int orient = presets[choice][i][3];

        for (int j = 0; j < len; j++) {
            if (orient == 1) field[x + j][y] = '#';
            else field[x][y + j] = '#';
        }
    }
}

bool Game::autoAttack(Game &enemy) {
    int x, y;
    do {
        x = rand() % SIZE;
        y = rand() % SIZE;
    } while (enemy.field[x][y] == 'X' || enemy.field[x][y] == '*');

    if (enemy.field[x][y] == '#') {
        enemy.field[x][y] = 'X';
        battleField[x][y] = 'X';
        return true;
    } 
    else{
        enemy.field[x][y] = '*';
        battleField[x][y] = '*';
        return false;
    }
    
}